<h3>Este es un plugin personalizado destinado a la página de Herbolario Ágave que implementa la opción de 
    añadir facilmente varios tipos de calculadoras relacionadas con la nutrición y el cuidado dietético
    mediante shortcodes. </h3>
<p>Para añadir las diferentes aplicaciones del plugin, inserta los siguientes shortcodes en la parte de la web
    donde los necesites:</p>
    <hr>
    <h3>Calculadora IMC:</h3><p>[imc-calc]</p>
    <h3>Calculadora Avanzada:</h3><p>[advanced-calc]</p>
    <h3>Calculadora Cineantropometría:</h3><p>[cineantropometria-calc]</p>
